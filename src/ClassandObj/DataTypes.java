package ClassandObj;

public class DataTypes {

    public static void main(String[] args) {
        int age = 24;
        double salary = 50000;
        char grade = 'A';
        boolean Active = true;


        System.out.println("age: " +age);
        System.out.println("salary: " +salary);
        System.out.println("grade: " +grade);
        System.out.println("Active: " +Active);

    }
}